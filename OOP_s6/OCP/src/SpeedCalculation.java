/**
 * Калькулятор допустимой скорости транспорта
 */
public class SpeedCalculation {
    /**
     * Калькулятор скорости
     * @param vehicle транспортное средство
     * @return возвращает допустимую скорость для указанного транспортного средства
     */
    public double calculateAllowedSpeed(Vehicle vehicle) {
        return vehicle.calculateAllowedSpeed();
    }
}
